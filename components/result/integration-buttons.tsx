"use client"

import { useState } from "react"
import { Mail } from "lucide-react"
import { Button } from "@/components/ui/button"
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
} from "@/components/ui/dialog"
import { track } from "@/lib/analytics"
import type { ExtractedReceipt } from "@/lib/receipt"
import { formatCurrency, formatDate, formatText } from "./format"

interface IntegrationButtonsProps {
  data: ExtractedReceipt
  requestId: string
}

interface Integration {
  id: string
  name: string
}

const PLACEHOLDER_INTEGRATIONS: Integration[] = [
  { id: "sap_concur", name: "SAP Concur" },
  { id: "abacus", name: "Abacus" },
  { id: "ubs_paychex", name: "UBS Paychex" },
  { id: "personio", name: "Personio" },
  { id: "datev", name: "DATEV" },
  { id: "bexio", name: "Bexio" },
  { id: "quickbooks", name: "QuickBooks" },
  { id: "xero", name: "Xero" },
  { id: "dynamics_365", name: "Microsoft Dynamics 365" },
]

function buildMailtoBody(data: ExtractedReceipt): string {
  const lines = [
    `Vendor: ${formatText(data.vendor.name)}`,
    `Date: ${formatDate(data.transaction.date)}`,
    `Total: ${formatCurrency(data.totals.total, data.totals.currency)}`,
    `VAT Number: ${formatText(data.vendor.vat_number)}`,
    `Receipt #: ${formatText(data.transaction.receipt_number)}`,
    `Payment: ${formatText(data.transaction.payment_method)}`,
    `Category: ${formatText(data.category_guess)}`,
  ]
  if (data.line_items.length > 0) {
    lines.push("", "Line items:")
    for (const item of data.line_items) {
      lines.push(
        `- ${item.description} (qty ${item.quantity ?? 1}) — ${formatCurrency(
          item.total_price,
          data.totals.currency
        )}`
      )
    }
  }
  lines.push("", "— Generated by ScanWise")
  return lines.join("\n")
}

export function IntegrationButtons({ data, requestId }: IntegrationButtonsProps) {
  const [activeIntegration, setActiveIntegration] = useState<Integration | null>(null)
  const [openedAt, setOpenedAt] = useState<number>(0)

  const handlePlaceholder = (integration: Integration) => {
    track("scanwise_integration_clicked", {
      integration_id: integration.id,
      request_id: requestId,
    })
    setActiveIntegration(integration)
    setOpenedAt(performance.now())
  }

  const handleEmailHr = () => {
    track("scanwise_integration_clicked", {
      integration_id: "email_hr",
      request_id: requestId,
    })
    const subject = encodeURIComponent(
      `Receipt — ${data.vendor.name ?? "Unknown vendor"} — ${data.transaction.date ?? ""}`
    )
    const body = encodeURIComponent(buildMailtoBody(data))
    window.location.href = `mailto:?subject=${subject}&body=${body}`
  }

  const handleClose = (open: boolean) => {
    if (!open && activeIntegration) {
      track("scanwise_integration_modal_dismissed", {
        integration_id: activeIntegration.id,
        dwell_time_ms: Math.round(performance.now() - openedAt),
      })
      setActiveIntegration(null)
    }
  }

  return (
    <>
      <div className="grid grid-cols-2 gap-2 sm:grid-cols-3">
        <Button
          variant="secondary"
          size="sm"
          onClick={handleEmailHr}
          className="gap-2 justify-start"
          data-integration-id="email_hr"
        >
          <Mail className="h-4 w-4" />
          Email to HR
        </Button>
        {PLACEHOLDER_INTEGRATIONS.map((integration) => (
          <Button
            key={integration.id}
            variant="outline"
            size="sm"
            onClick={() => handlePlaceholder(integration)}
            className="justify-start"
            data-integration-id={integration.id}
          >
            {integration.name}
          </Button>
        ))}
      </div>

      <Dialog open={!!activeIntegration} onOpenChange={handleClose}>
        <DialogContent>
          <DialogHeader>
            <DialogTitle>
              {activeIntegration?.name} integration is coming soon
            </DialogTitle>
            <DialogDescription>
              We&apos;re tracking interest to prioritize what we build next. Thanks for letting
              us know this matters to you!
            </DialogDescription>
          </DialogHeader>
        </DialogContent>
      </Dialog>
    </>
  )
}
